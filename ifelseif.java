package basicsprograms;

public class ifelseif 
{

	public static void main(String[] args) 
	{
		
		int a=100;
		int b=200;
		int c=300;
		
		if(a!=b) //1st true will execute
		{
			System.out.println("If Block");
		}
		else if(b!=c)
		{
			System.out.println("Else If Block");
		}
		else
		{
			System.out.println("Else Block");
		}
		
		
	}

}
